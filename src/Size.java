public class Size {

    private int height;
    private int width;

    Size(int height, int width){
        height = this.height;
        width = this.width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}
